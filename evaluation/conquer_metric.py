import pandas as pd
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score
from sklearn.metrics import confusion_matrix

file_path = "eval_data_conquer.xlsx"

gold = pd.read_excel(
    file_path,
    sheet_name="gold",
    engine="openpyxl"
)

sheets = ["S1", "S2", "S3", "S4", "S5"]

preds = []

for s in sheets:
    df = pd.read_excel(
        file_path,
        sheet_name=s,
        engine="openpyxl"
    )
    df["system"] = s
    preds.append(df)

pred = pd.concat(preds, ignore_index=True)

# extracting construction columns
meta_cols = ["sent_id", "sentence", "system"]

construction_cols = [
    c for c in gold.columns
    if c not in meta_cols
]

gold[construction_cols] = gold[construction_cols].fillna(0).astype(int)
pred[construction_cols] = pred[construction_cols].fillna(0).astype(int)

### long form
gold_long = gold.melt(
    id_vars=["sent_id", "sentence"],
    value_vars=construction_cols,
    var_name="construction",
    value_name="gold"
)

pred_long = pred.melt(
    id_vars=["sent_id", "sentence", "system"],
    value_vars=construction_cols,
    var_name="construction",
    value_name="pred"
)

eval_df = pred_long.merge(
    gold_long,
    on=["sent_id", "sentence", "construction"],
    how="left"
)

print(eval_df["gold"].unique())
print(eval_df["pred"].unique())

tn, fp, fn, tp = confusion_matrix(
    eval_df["gold"],
    eval_df["pred"]
).ravel()

print("TP:", tp)
print("FP:", fp)
print("FN:", fn)
print("TN:", tn)

### overall
overall = {
    "accuracy": accuracy_score(eval_df["gold"], eval_df["pred"]),
    "precision": precision_score(eval_df["gold"], eval_df["pred"], zero_division=0),
    "recall": recall_score(eval_df["gold"], eval_df["pred"], zero_division=0),
    "f1": f1_score(eval_df["gold"], eval_df["pred"], zero_division=0),
}

print(overall)

### user
by_user = eval_df.groupby("system").apply(
    lambda g: pd.Series({
        "accuracy": accuracy_score(g["gold"], g["pred"]),
        "precision": precision_score(g["gold"], g["pred"], zero_division=0),
        "recall": recall_score(g["gold"], g["pred"], zero_division=0),
        "f1": f1_score(g["gold"], g["pred"], zero_division=0),
    })
).reset_index()

print(by_user)

### construction
by_construction = eval_df.groupby("construction").apply(
    lambda g: pd.Series({
        "accuracy": accuracy_score(g["gold"], g["pred"]),
        "precision": precision_score(g["gold"], g["pred"], zero_division=0),
        "recall": recall_score(g["gold"], g["pred"], zero_division=0),
        "f1": f1_score(g["gold"], g["pred"], zero_division=0),
    })
).reset_index()

print(by_construction)

### user x construction
by_user_construction = eval_df.groupby(["system", "construction"]).apply(
    lambda g: pd.Series({
        "accuracy": accuracy_score(g["gold"], g["pred"]),
        "precision": precision_score(g["gold"], g["pred"], zero_division=0),
        "recall": recall_score(g["gold"], g["pred"], zero_division=0),
        "f1": f1_score(g["gold"], g["pred"], zero_division=0),
    })
).reset_index()

print(by_user_construction)

with pd.ExcelWriter("conquer_eval_results.xlsx") as writer:

    pd.DataFrame([overall]).to_excel(
        writer,
        sheet_name="overall",
        index=False
    )

    by_user.to_excel(
        writer,
        sheet_name="by_user",
        index=False
    )

    by_construction.to_excel(
        writer,
        sheet_name="by_construction",
        index=False
    )

    by_user_construction.to_excel(
        writer,
        sheet_name="by_user_construction",
        index=False
    )

